/**
 * @fileoverview Enum ролей пользователей системы.
 *
 * Определяет допустимые роли для авторизации и контроля доступа.
 * Используется в User entity и Guards для проверки прав доступа.
 */

/**
 * Роли пользователей в системе.
 *
 * @enum {string}
 * @property {string} USER - Обычный пользователь с базовыми правами
 * @property {string} ADMIN - Администратор с полными правами доступа
 */
export enum UserRole {
  /** Обычный пользователь с базовыми правами. */
  USER = "user",

  /** Администратор с полными правами доступа. */
  ADMIN = "admin",
}
