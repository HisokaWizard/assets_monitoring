/**
 * @fileoverview Сущность актива для базы данных.
 *
 * Этот файл определяет модель данных для активов (акций, криптовалюты и т.д.)
 * с использованием TypeORM. Сущности в NestJS представляют таблицы базы данных
 * и используются для взаимодействия с данными через репозитории.
 *
 * Декоратор @Entity указывает, что класс является сущностью базы данных.
 * @PrimaryGeneratedColumn создает автоинкрементный первичный ключ.
 * @Column определяет столбцы таблицы с типами данных.
 */

import { Entity, Column, PrimaryGeneratedColumn } from 'typeorm';

/**
 * Сущность актива.
 *
 * Представляет финансовый актив в системе мониторинга активов.
 * Содержит информацию о названии, символе, цене и типе актива.
 *
 * @Entity декоратор регистрирует класс как таблицу в базе данных,
 * позволяя TypeORM автоматически создавать и управлять схемой.
 */
@Entity()
export class Asset {
  /**
   * Уникальный идентификатор актива.
   *
   * @PrimaryGeneratedColumn декоратор создает первичный ключ с автоинкрементом,
   * обеспечивая уникальность каждой записи в таблице.
   */
  @PrimaryGeneratedColumn()
  id: number;

  /**
   * Название актива (например, "Apple Inc.").
   *
   * @Column декоратор определяет столбец в таблице с типом VARCHAR.
   */
  @Column()
  name: string;

  /**
   * Торговая символ актива (например, "AAPL" для Apple).
   */
  @Column()
  symbol: string;

  /**
   * Текущая цена актива.
   *
   * Использует тип 'decimal' для точного хранения денежных значений.
   */
  @Column('decimal')
  price: number;

  /**
   * Тип актива (например, "stock", "crypto").
   *
   * Определяет категорию актива для классификации и фильтрации.
   */
  @Column()
  type: string; // e.g., stock, crypto, etc.
}
