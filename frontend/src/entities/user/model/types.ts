/**
 * @fileoverview Типы и интерфейсы для сущности пользователя.
 *
 * Определяет модель данных пользователя в системе.
 */

/**
 * Роль пользователя в системе.
 */
export type UserRole = 'user' | 'admin';

/**
 * Модель пользователя.
 *
 * Представляет данные пользователя, полученные с backend.
 */
export interface User {
  /**
   * Уникальный идентификатор пользователя.
   */
  id: number;

  /**
   * Email пользователя.
   */
  email: string;

  /**
   * Роль пользователя в системе.
   */
  role: UserRole;

  /**
   * Дата создания аккаунта.
   */
  createdAt: string;

  /**
   * Дата последнего обновления.
   */
  updatedAt: string;
}
